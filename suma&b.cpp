#include <iostream>
using namespace std;

int main() {
    float a, b, suma;

    cout << "Ingresa el primer numero: ";
    cin >> a;
    cout << "Ingresa el segundo numero: ";
    cin >> b;

    suma = a + b;

    cout << "La suma de " << a << " y " << b << " es: " << suma << endl;

    return 0;
}